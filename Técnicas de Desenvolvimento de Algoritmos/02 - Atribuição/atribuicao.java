// Declara uma classe pública chamada "atribuicao".
public class atribuicao {

    // Método principal (main), o ponto de entrada do programa.
    public static void main(String[] args) {

        // Declara três variáveis (a, b, c) do tipo double, que podem armazenar números com casas decimais.
        double a, b, c;

        // Atribui o valor 12.05 à variável 'a'. O sinal de '=' é o operador de atribuição.
        a = 12.05;

        // Atribui o valor 5.20 à variável 'b'.
        b = 5.20;

        // Multiplica o valor contido em 'a' pelo valor contido em 'b' e atribui o resultado à variável 'c'.
        c = a * b;

        // Imprime o resultado no console. O sinal de '+' junta (concatena) o texto com o valor da variável 'c'.
        System.out.println("O valor de c é " + c );

    } // Fim do método main

} // Fim da classe atribuicao