public class simulado16 {

    public static void main(String[] args) {

        int i = 1;

        do {
            // 1. Executa o bloco
            System.out.println(i);

            // 2. Incrementa (prepara o próximo)
            i++;

        } while (i <= 10); // ATENÇÃO: No do-while, o while leva ponto e vírgula no final!
    }
}